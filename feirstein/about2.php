			<article class="hidden" data-person="steven-soderbergh">
				<div class="picture" style="background-image:url('http://www.brooklyn.cuny.edu/web/academics/schools/mediaarts/schools/feirstein/build/images/Steven_Soderbergh.jpg')">
				</div>
				<div class="bio">
					<h5>Steven Soderbergh</h5>
					<p>Steven Soderbergh is a writer, director, producer, cinematographer and editor. His television series, <em>The Knick</em>, is currently airing on Cinemax. He earned an Oscar nomination for Best Original Screenplay for his directorial debut, <em>sex, lies, and videotape</em>, and the Academy Award in 2000 for directing <em>Traffic</em>, the same year he was nominated for <em>Erin Brockovich</em>.</p>
					<p>Among his other credits are the films <em>Side Effects</em>, <em>Magic Mike</em>, <em>Haywire</em>, <em>Contagion</em>, <em>And Everything Is Going Fine</em>, <em>The Girlfriend Experience</em>, <em>The Informant</em>, <em>Che</em>, the <em>Ocean’s</em> trilogy, <em>The Good German</em>, <em>Bubble</em>, <em>Solaris</em>, <em>Full Frontal</em>, <em>The Limey</em>, <em>Out of Sight</em>, <em>Grey’s Anatomy</em>, <em>Schizopolis</em>, <em>The Underneath</em>, <em>King of the Hill</em> and <em>Kafka</em>.</p>
					<p>Soderbergh’s television film <em>Behind the Candelabra</em>, for which he won a 2013 Emmy Award for Outstanding Directing, debuted on HBO in May 2013. In 2009, he created and directed the play <em>Tot Mom</em> for the Sydney Theatre Company. While in Sydney he also directed the film <em>The Last Time I Saw Michael Gregg</em>. In April 2014, he directed the world premiere of Scott Burns’ new play, <em>The Library</em>, at New York’s Public Theater.</p>
