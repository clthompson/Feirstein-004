				<div class="bio">
					<h5>Ethan Hawke</h5>
					<p>Ethan Hawke is an accomplished actor, screenwriter, film director, theater director and novelist. He has appeared in more than 40 films, including <em>Dead Poets Society</em>, <em>Before Sunrise</em>, <em>Before Sunset</em>, <em>Before Midnight</em>, <em>Reality Bites</em>, <em>Gattaca</em>, <em>Training Day</em> and <em>Boyhood</em>. His numerous stage credits include, as an actor: <em>The Coast of Utopia</em>, <em>Henry IV</em>, <em>The Winter's Tale</em>, <em>The Cherry Orchard</em>, <em>Hurlyburly</em>, <em>Macbeth</em> and <em>Blood From a Stone</em> (2011 Obie Award winner); and as a director: <em>Things We Want</em>, <em>A Lie of the Mind</em> and <em>Clive</em>.</p>
					<p>Hawke has written two novels, <em>The Hottest State</em> and <em>Ash Wednesday</em>, the former of which he adapted and directed for film. He has also been nominated for a Tony Award, Academy Awards for both acting and writing, and a Drama Desk Award for both acting and directing.</p>
