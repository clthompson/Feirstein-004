				<div class="bio">
					<h5>Douglas C. Steiner</h5>
					<p>Douglas C. Steiner, chairman of Steiner Studios, has led the fight to re-establish New York's position as a world-class entertainment production center. Not only did he build Steiner Studios, New York's first Hollywood-style film and television production facility, but he also spearheaded the creation of the New York State Film Production Credit and each of its four renewals and expansions.</p>
					<p>Steiner has served on panels for Crain's New York Business and the New York City Office of the Mayor, and he is the recipient of awards from the Brooklyn International Film Festival as well as numerous New York political organizations. In his other job as a real estate developer, he has built and continues to lease and manage millions of square feet of commercial property. He is currently developing more than $600 million of residential and studio properties.</p>
					<p>Steiner serves on the boards of BRIC Arts Media Brooklyn, the Downtown Brooklyn Partnership and the Open Space Alliance for North Brooklyn. He has an A.B. in English from Stanford University, where he was editor-in-chief of the <em>Stanford Chaparral</em>, and was awarded an honorary Ph.D. from Brooklyn College.</p>
