				<div class="bio">
					<h5>Darren Aronofsky</h5>
					<p>Academy Award–nominated director Darren Aronofsky was born and raised in Brooklyn, N.Y.</p>
					<p>His most recent film, <em>Noah</em> (2014), starred Russell Crowe, Jennifer Connelly, Emma Watson and Anthony Hopkins. It was hailed by Richard Roeper as "one of the most dazzling and unforgettable Biblical epics ever put on film."</p>
					<p>In 2011, Aronofsky directed <em>Black Swan</em>, which earned Natalie Portman an Academy Award for Best Actress and Aronofsky a nomination for Best Director. In 2008, he directed <em>The Wrestler</em>, recipient of the Golden Lion at the Venice Film Festival. Mickey Rourke and Marisa Tomei received Academy Award nominations for their performances; Rourke won a Golden Globe for Best Actor — Drama, and Bruce Springsteen won for Best Original Song. Aronofsky also directed <em>The Fountain</em> (2006), starring Hugh Jackman and Rachel Weisz, and <em>Requiem for a Dream</em> (2000), starring Jared Leto, Ellen Burstyn and Jennifer Connolly. His first feature, <em>π</em> (1998), won the Director's Award at Sundance Film Festival.</p>
					<p>Aronofsky is a graduate of Harvard University and the American Film Institute.</p>
				</div>
