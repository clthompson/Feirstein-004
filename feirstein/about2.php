				<div class="bio">
					<h5>Joana Vicente</h5>
					<p>Joana Vicente has been the executive director of the Independent Filmmaker Project (IFP), the oldest and largest nonprofit organization for independent filmmakers in the United States, since 2009. Under Vicente's leadership, the IFP was most recently bestowed the honor of developing and operating the Made in NY Media Center after a Request for Proposal was issued by the Mayor's Office of Media and Entertainment and the New York City Economic Development Corporation. The state-of-the-art Media Center brings together professionals from the film, television, advertising, new media, gaming, marketing and branding industries for collaboration and new opportunities. It opened in the DUMBO neighborhood of Brooklyn, N.Y., in 2013.</p>
					<p>Prior to the IFP, Vicente and her partner Jason Kliot produced and executive produced more than 40 films by such acclaimed directors as Jim Jarmusch, Miguel Arteta, Brian De Palma, Hal Hartley, Steven Soderbergh, Nicole Holofcener and Todd Solondz. She has co-founded three separate, unique film production entities over the course of her career — Open City Films; Blow Up Pictures, the first digital production company in the United States; and HDNet Films, an award-winning digital production company founded with Mark Cuban and Todd Wagner. Among the many films that Vicente and Kliot have produced are Tony Bui's Sundance Grand Jury Prize and Audience Award winner, <em>Three Season</em>; Jim Jarmusch's cult classic <em>Coffee and Cigarettes</em>; Niels Mueller's <em>The Assassination of Richard Nixon</em>; Todd Solondz's Sundance Grand Jury Prize-winning <em>Welcome to the Dollhouse</em>; Brian De Palma's controversial <em>Redacted</em> and Alex Gibney's Academy Award–nominated <em>Enron: The Smartest Guys in the Room</em>. Vicente has served on the jury at Sundance and many other festivals.</p>
					<p>Vicente's films have garnered numerous accolades and awards, including 23 Independent Spirit Award nominations and four wins. In 2007, Vicente was the recipient of the Made in NY Award for individuals who have made outstanding contributions to New York City's entertainment industry. Vicente graduated from the master’s program at The Catholic University of Portugal with a degree in philosophy and began her career as the press attaché for the Portuguese delegate and former prime minister of Portugal at the European Parliament. She later became a radio news producer for the United Nations before turning her attention to film.</p>
				</div>
