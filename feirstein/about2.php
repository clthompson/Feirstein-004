			<article class="hidden" data-person="fisher-stevens">
				<div class="picture" style="background-image:url('http://www.brooklyn.cuny.edu/web/academics/schools/mediaarts/schools/feirstein/build/images/Fisher_Stevens.jpg')">
				</div>
				<div class="bio">
					<h5>Fisher Stevens</h5>
					<p>Fisher Stevens has been in the entertainment business for more than 30 years. His versatility in the industry is evident from his wide range of credits, from acting to producing to directing, and from film to television to theater and working with the United Nations.</p>
					<p>Stevens began his acting career in New York, appearing in more than 40 Broadway and off-Broadway shows. His first big break came when appearing in the Tony Award-winning production of <em>Torch Song Trilogy</em>, playing David, the adopted son, opposite Harvey Fierstein. He parlayed his stage success into acting in such films as <em>The Flamingo Kid</em>, <em>Short Circuit</em> and its sequel, <em>Hackers</em>, <em>Super Mario Brothers</em>, <em>Awake</em>, <em>Factotum</em>, <em>The Experiment</em> and <em>Henry's Crime</em>. Most recently he starred in the acclaimed Wes Anderson film <em>The Grand Budapest Hotel</em>. On television, Stevens was a series regular on Fox’s <em>Key West</em> and CBS’s <em>Early Edition</em>. He had recurring roles on <em>Lost</em> and <em>Damages</em> as well as guest-starring roles on numerous episodes of <em>Law & Order</em>, <em>Californication</em>, <em>Ugly Betty</em> and <em>It’s Always Sunny in Philadelphia</em>, among others.</p>
					<p>In 1986 Stevens co-founded the theater company Naked Angels, which just celebrated its 25th year, where he has produced, directed and acted in many productions. In 1996 he co-founded GreeneStreet Films, where he had his film directorial debut with <em>Just a Kiss</em>, starring Marisa Tomei, Kyra Sedgwick and Taye Diggs. Stevens has produced more than 15 films while involved with GreeneStreet, including the Academy Award–nominated <em>In the Bedroom</em>, <em>A Prairie Home Companion</em>, <em>Piñero</em>, <em>Swimfan</em>, <em>Uptown Girls</em> and the acclaimed documentary <em>Once in a Lifetime</em>.</p>
					<p>After <em>Once in a Lifetime</em>, Stevens became interested in documentary filmmaking, producing and co-directing the 2008 Independent Spirit Award–winning documentary <em>Crazy Love</em>. He then went on to produce the 2010 Academy Award–winning documentary <em>The Cove</em>, about the dolphin slaughters taking place in Taiji, Japan.</p>
					<p>In March 2010, Stevens co-founded Insurgent Media with Andrew Karsch and Erik Gordon. At present, they have completed the 2012 SXSW Grand Jury Prize–winning documentary <em>Beware of Mr. Baker, Before the Spring, After the Fall</em>, the critically acclaimed <em>Blank City</em> (about the 1970s New York underground film scene) and <em>Woody Allen: A Documentary</em>, which premiered on PBS American Masters and was directed by Robert Weide. Stevens made his Broadway directorial debut with John Leguizamo's <em>Ghetto Klown</em> at the Lyceum Theatre, which was also adapted for HBO. He recently directed the feature film <em>Stand Up Guys</em> for Lionsgate, starring Al Pacino, Christopher Walken and Alan Arkin, as well as music videos for Jon Bon Jovi’s latest album.</p>
				</div>
