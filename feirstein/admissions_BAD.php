				<aside><h3>MFA</h3><h5>in cinema <br/>arts</h5></aside>
				<article>
					<h5>Admissions Guidelines for M.F.A. in Cinema Arts</h5>
					<p>Applications to the M.F.A. in Cinema Arts program will be considered based on demonstrated evidence of the following:</p>
					<ul>
						<li>Undergraduate record of achievement with a minimum grade point average of 3.00</li>
						<li>An abiding commitment to the art of filmmaking</li>
						<li>A clear intention to pursue a career in the moving image arts</li>
						<li>A strong interest in current intellectual and cultural issues</li>
						<li>A commitment to undertake three years of full-time graduate study</li>
					</ul>
					<p>Students are accepted into only one filmmaking discipline: producing, directing, screenwriting, cinematography or post-production. Although classes are required across the different disciplines, once accepted, you will not be able to transfer into another discipline. You may, however, apply to up to two disciplines, indicating your first-choice discipline. Students are admitted on a full-time basis only and required to complete a total of 66 credits over three years.</p>
					<p>The Office of Graduate Admissions manages applications to the M.F.A. in Cinema Arts. Only complete applications are forwarded to the Feirstein Graduate School of Cinema.</p>
					<strong>Application Form</strong>
					<p><a href="http://www.brooklyn.cuny.edu/web/academics/schools/mediaarts/schools/feirstein/feirsteingraduateapplication.php" target="_blank">Apply online</a>. Supporting documents, such as official transcripts and essays, must be mailed to the Office of Graduate Admissions.</p>
					<strong>Application Fee</strong>
					<p>Pay the nonrefundable $125 application fee.</p>
					<strong>Official Transcripts</strong>
					<p>Submit official transcripts from all universities and colleges attended. </p>
					<strong>Letters of Recommendation</strong>
					<p>Three letters of recommendation are required from individuals who can assess your potential for graduate study. Register your recommenders in the online application system so that they can submit their letters electronically.</p>
					<strong>Statement of Purpose</strong>
					<p>Write a three- to four-page (double-spaced 12-point font) statement explaining your purpose in undertaking graduate study in cinema arts. This is your opportunity to articulate your goals, interests and career plans as they relate to undertaking an M.F.A. in cinema arts. We are trying to gain a sense of who you are and the distinctive experiences and interests that have motivated you to pursue this degree.</p>
					<strong>Résumé/CV</strong>
					<p>Submit a copy of your résumé or CV, including your education to date, employment and other relevant information.</p>
					<strong>GRE</strong>
					<p>The <a href="http://www.ets.org/gre" target="_blank">Graduate Record Examination</a> (GRE) is not required.</p>
					<h5>Portfolio</h5>
					<p>You must prepare and submit a portfolio of creative work via the online application system, Slideroom. Please note that, upon submitting your creative portfolio Slideroom will charge a $6 fee for applicants to use this service (you may pay this fee with a credit or debit card).</p>
					<h5>Filmmaking Disciplines</h5>
					<strong>Producing</strong>
					<p>1. Choose a feature film that you admire and explain in five pages or less (double-spaced 12-point font) what specifically impressed you about the film in terms of content and production <strong><em>or;</em></strong><br>
					2. In 10 pages or less, (double-spaced 12-point font) write a treatment for a feature film that you would like to produce, including one page explaining why.</p>
					<strong>Directing</strong>
					<p>1. Submit a short film that you have personally directed, not to exceed 15 minutes <strong><em>or;</em></strong><br>
					2. In 10 pages or less, (double-spaced 12-point font) write a treatment for a feature film that you would like to direct, including one page explaining why.</p>
					<strong>Screenwriting</strong>
					<p>1. Submit a screenplay that you have personally written, not to exceed 20 pages in length, correctly formatted <strong><em>or;</em></strong><br>
					2. In 10 pages or less (double-spaced 12-point font) write a treatment for a feature film that you would like to write, including one page explaining why.</p>
					<strong>Cinematography</strong>
					<p>1. Submit a short film that you have personally shot, not to exceed 15 minutes <strong><em>or;</em></strong><br>
					2. Submit clips from work that you personally have shot, not to exceed a total running time of 10 minutes.</p>
					<strong>Post-production</strong>
					<p>1. Submit a short film that you have personally edited, not to exceed 15 minutes <strong><em>or;</em></strong><br>
					2. Submit clips from work that you have personally edited, not to exceed a total running time of 10 minutes.</p>
					<p><em>Portfolio submission materials will not be returned to applicants. We recommend submitting copies only.</em></p>
					<strong>Instructions for Uploading Portfolio to Slideroom</strong>
					<p>Register and <a href="http://feirsteincinema.slideroom.com" target="_blank">set up an account on Slideroom</a>. Once you have registered to start a portfolio via Slideroom, you may step away and come back at any time, until the final deadline. Please ensure that you save your progress after each visit, and do not click to submit until you are certain that you have uploaded the required components. Once you click submit, you may no longer edit your portfolio. Once you submit your portfolio, Slideroom will send you an e-mail to confirm receipt of your materials.</p>
					<p>The submission of video can contain one or more selections as long as the total running time does not exceed 30 minutes. You may upload up to 10 minutes of video on to the media page within Slideroom. If the total running time of your video sample exceeds 10 minutes, a link to your work must be provided on the media page. Please test your video prior to submission. If you are submitting a link, please be sure no downloads or passwords are required. For technical assistance, please <a href="mailto:support@slideroom.com" target="_blank">e-mail Slideroom</a>.</p>
					<h5>DEADLINES</h5>
					<p>Applications open on November 1, 2014. Deadline for priority admissions is December 1, 2014. Applications received after the deadline will be reviewed on a rolling admissions basis until the class is full. Each track is capped at 14 students, except for screenwriting, where up to 20 students will be admitted.</p>
