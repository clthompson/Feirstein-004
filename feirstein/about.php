					<h5>Roy Furman</h5>
					<p>Roy Furman is vice chairman of Jefferies Group LLC and chairman of Jefferies Capital Partners, a group of private equity funds. In 1973, Furman co-founded Furman Selz, an international investment banking, institutional brokerage and money-management firm. He served as president and CEO until 1997, when the firm was sold to ING. For decades, he has been a Wall Street analyst and investment banker involved with the entertainment industry.</p>
					<p>Furman is vice chairman of Lincoln Center for the Performing Arts and chairman emeritus of the Film Society of Lincoln Center. He served as national finance chairman for the Democratic National Committee in 1992–93, was national fund chair for Harvard Law School for two years, and was chairman of the Brooklyn College Foundation for four years.</p>
					<p>Furman has produced dozens of Broadway musicals and plays, including numerous Tony winners. Currently on Broadway: The Book of Mormon, Cinderella, Rocky, Mothers and Sons, A Raisin in the Sun, and Bullets Over Broadway. Upcoming productions include Houdini, The Last Ship, and An American in Paris.</p>
