				<div class="bio">
					<h5>Darren Aronofsky</h5>
					<p>Academy Award–nominated director Darren Aronofsky was born and raised in Brooklyn, N. Y. His most recent film, Noah, was released to critical acclaim and grossed more than $340 million worldwide. Starring Russell Crowe, Anthony Hopkins, Jennifer Connelly and Emma Watson, it is an adaptation of the story of Noah’s ark, which Richard Roeper called “one of the most dazzling and unforgettable Biblical epics ever put on film.”</p>
					<p>Prior to Noah, Aronofsky directed Black Swan, which won Natalie Portman the Academy Award for Best Actress and received four other nominations, including one for Best Picture. The film received other accolades, appeared on more than 200 critical Top 10 lists, and swept the 2011 Independent Spirit Award with wins for Best Film, Best Director, Best Actress and Best Cinematography. The box office phenomenon grossed more than $320 million worldwide.</p>
					<p>In 2008, Aronofsky directed The Wrestler. The film premiered at the Venice Film Festival, where it won the esteemed Golden Lion, making it only the third American film in history to win this grand prize. The Wrestler won Best Feature at the Independent Spirit Awards and garnered Academy Award nominations for both Mickey Rourke and Marisa Tomei. Golden Globes were given to Rourke for his iconic performance and to Bruce Springsteen for his original track The Wrestler. Aronofsky also wrote and directed The Foundation, a science-fiction romance starring Hugh Jackman and Rachel Weisz. In 2000, he premiered Requiem for a Dream at the Cannes Film Festival. The film was named to more than 150 Top 10 lists, including The New York Times, Rolling Stone, Entertainment Weekly and the American Film Institute, and five Independent Spirit Award nominations, including one for Best Director and a win for Best Actress, Ellen Burstyn. Aronofsky’s first feature, π, won the Director’s Award at the 1998 Sundance Film Festival and an Independent Spirit Award for Best First Screenplay. Among his honors, the American Film Institute gave Aronofsky the prestigious Franklin J. Schaffner Alumni Medal, the Stockholm Film Festival presented him the Golden Horse Visionary Award, and he has won three Independent Spirit Awards.</p>
				</div>
