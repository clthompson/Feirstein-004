					<h5>Don Buchwald</h5>
					<p>Don Buchwald is the founder of Buchwald & Associates, a full-service talent and literary agency with offices in New York and Los Angeles, whose roster of clientele includes actors, directors, producers, writers and broadcasters, most notably Howard Stern.</p>
					<p>Born and raised in Brooklyn, N.Y., Buchwald now lives in Manhattan with his wife, Maggie; two daughters, Julia and Laura; and two grandchildren, Sebastian and Scarlett. A member of the board of governors of the Friars Club, he has been a member of the board of trustees of Brooklyn College for 15 years and is the first recipient of the college’s Theater Alumnus Award (1998). He is a major contributor to the Leonard & Claire Tow Center for the Performing Arts, the Barry R. Feirstein Graduate School of Cinema, and the Don Buchwald Internship Program in Theater, Television and Radio.</p>
