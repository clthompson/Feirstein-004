				<div class="bio">
					<h5>Ethan Hawke</h5>
					<p>Ethan Hawke is an accomplished actor, screenwriter, film director, theater director and novelist. He has appeared in more than 40 films, including Dead Poets Society, Before Sunrise, Before Sunset, Before Midnight, Reality Bites, Gattaca,Training Day and Boyhood. His numerous stage credits include, as an actor: The Coast of Utopia, Henry IV, The Winter's Tale, The Cherry Orchard, Hurlyburly, Macbeth and Blood From a Stone (2011 Obie Award winner); and as a director: Things We Want, A Lie of the Mind and Clive.</p>
					<p>Hawke has written two novels, The Hottest State and Ash Wednesday, the former of which he adapted and directed for film. He has also been nominated for a Tony Award, Academy Awards for both acting and writing, and a Drama Desk Award for both acting and directing.</p>
				</div>
			</article>
			<article class="hidden" data-person="michael-lynne">
				<div class="picture" style="background-image:url('http://www.brooklyn.cuny.edu/web/feirsteinbeta/feirstein/build/images/Michael_Lynne.jpg')">
				</div>
				<div class="bio">
					<h5>Michael Lynne</h5>
					<p>Michael Lynne is a principal of Unique Features LLC, a film and television development and production company. Prior to Unique Features, Lynne was co-chairman and co-CEO of New Line Cinema Corporation. During his long association with the company and its founder, Robert Shaye, he was a driving force in its becoming the most successful, fully integrated independent film company in the world. In addition to many other films, Lynne was an executive producer of New Line’s historically successful The Lord of the Rings trilogy.</p>
					<p>Lynne is a long-time trustee of the Brooklyn College Foundation and also sits on the boards of the Museum of Modern Art, Citymeals-on-Wheels and Guild Hall of East Hampton. He is a member of the board of visitors of Columbia Law School, where he received his J.D. degree in 1964. He is a director of the publicly traded REIT, Vornado Realty Trust and IMAX Corporations, and is the owner of the North Fork’s premier winery and vineyard, Bedell Cellars.</p>
				</div>
			</article>
			<article class="hidden" data-person="steven-soderbergh">
				<div class="picture" style="background-image:url('http://www.brooklyn.cuny.edu/web/feirsteinbeta/feirstein/build/images/Steven_Soderbergh.jpg')">
				</div>
				<div class="bio">
					<h5>Steven Soderbergh</h5>
					<p>Steven Soderbergh is a writer, director, producer, cinematographer and editor. His television series, The Knick, is currently airing on Cinemax. He earned an Oscar nomination for Best Original Screenplay for his directorial debut, sex, lies, and videotape, and the Academy Award in 2000 for directing Traffic, the same year he was nominated for Erin Brockovich.</p>
					<p>Among his other credits are the films Side Effects, Magic Mike, Haywire, Contagion, And Everything Is Going Fine, The Girlfriend Experience, The Informant, Che, the Ocean’s trilogy, The Good German, Bubble, Solaris, Full Frontal, The Limey, Out of Sight, Grey’s Anatomy, Schizopolis, The Underneath, King of the Hill and Kafka.</p>
					<p>Soderbergh’s television film Behind the Candelabra, for which he won a 2013 Emmy Award for Outstanding Directing, debuted on HBO in May 2013. In 2009, he created and directed the play Tot Mom for the Sydney Theatre Company. While in Sydney he also directed the film The Last Time I Saw Michael Gregg. In April 2014, he directed the world premiere of Scott Burns’ new play, The Library, at New York’s Public Theater.</p>
				</div>
			</article>
			<article class="hidden" data-person="douglas-steiner">
				<div class="picture" style="background-image:url('http://www.brooklyn.cuny.edu/web/feirsteinbeta/feirstein/build/images/Douglas_Steiner.jpg')">
				</div>
				<div class="bio">
					<h5>Douglas Steiner</h5>
					<p>Douglas C. Steiner, chairman of Steiner Studios, has led the fight to re-establish New York's position as a world-class entertainment production center. Not only did he build Steiner Studios, New York's first Hollywood-style film and television production facility, but he also spearheaded the creation of the New York State Film Production Credit and each of its four renewals and expansions.</p>
					<p>Steiner has served on panels for Crain's New York Business and the New York City Office of the Mayor, and he is the recipient of awards from the Brooklyn International Film Festival as well as numerous New York political organizations. In his other job as a real estate developer, he has built and continues to lease and manage millions of square feet of commercial property. He is currently developing more than $600 million of residential and studio properties.</p>
					<p>Steiner serves on the boards of BRIC Arts Media Brooklyn, the Downtown Brooklyn Partnership and the Open Space Alliance for North Brooklyn. He has an A.B. in English from Stanford University, where he was editor-in-chief of the Stanford Chaparral, and was awarded an honorary Ph.D. from Brooklyn College.</p>
				</div>
			</article>
			<article class="hidden" data-person="fisher-stevens">
				<div class="picture" style="background-image:url('http://www.brooklyn.cuny.edu/web/feirsteinbeta/feirstein/build/images/Fisher_Stevens.jpg')">
				</div>
				<div class="bio">
					<h5>Fisher Stevens</h5>
					<p>Fisher Stevens has been in the entertainment business for more than 30 years. His versatility in the industry is evident from his wide range of credits, from acting to producing to directing, and from film to television to theater and working with the United Nations.</p>
					<p>Stevens began his acting career in New York, appearing in more than 40 Broadway and off- Broadway shows. His first big break came when appearing in the Tony Award-winning production of Torch Song Trilogy, playing David, the adopted son, opposite Harvey Fierstein. He parlayed his stage success into acting in such films as The Flamingo Kid, Short Circuit and its sequel, Hackers, Super Mario Brothers, Awake, Factotum, The Experiment and Henry’s Crime. Most recently he starred in the acclaimed Wes Anderson film The Grand Budapest Hotel. On television, Stevens was a series regular on Fox’s Key West and CBS’s Early Edition. He had recurring roles on Lost and Damages as well as guest-starring roles on numerous episodes of Law & Order, Californication, Ugly Betty and It’s Always Sunny in Philadelphia, among others.</p>
					<p>In 1986 Stevens co-founded the theater company Naked Angels, which just celebrated its 25th year, where he has produced, directed and acted in many productions. In 1996 he co-founded GreeneStreet Films, where he had his film directorial debut with Just a Kiss starring Marisa Tomei, Kyra Sedgwick and Taye Diggs. Stevens has produced more than 15 films while involved with GreeneStreet, including the Academy Award–nominated In the Bedroom, A Prairie Home Companion, Piñero, Swimfan, Uptown Girls and the acclaimed documentary Once in a Lifetime.</p>
					<p>After Once in a Lifetime, Stevens became interested in documentary filmmaking, producing and co-directing the 2008 Independent Spirit Award–winning documentary Crazy Love. He then went on to produce the 2010 Academy Award–winning documentary The Cove, about the dolphin slaughters taking place in Taiji, Japan.</p>
					<p>In March 2010, Stevens co-founded Insurgent Media with Andrew Karsch and Erik Gordon. At present, they have completed the 2012 SXSW Grand Jury Prize–winning documentary Beware of Mr. Baker, Before the Spring, After the Fall, the critically acclaimed Blank City (about the 1970s New York underground film scene) and Woody Allen: A Documentary, which premiered on PBS American Masters and was directed by Robert Weide. Stevens made his Broadway directorial debut with John Leguizamo’s Ghetto Klown at the Lyceum Theatre, which was also adapted for HBO. He recently directed the feature film Stand Up Guys for Lionsgate, starring Al Pacino, Christopher Walken and Alan Arkin, as well as music videos for Jon Bon Jovi’s latest album.</p>
				</div>
			</article>
			<article class="hidden" data-person="joana-vicente">
				<div class="picture" style="background-image:url('http://www.brooklyn.cuny.edu/web/feirsteinbeta/feirstein/build/images/Joana_Vicente.jpg')">
				</div>
				<div class="bio">
					<h5>Joana Vicente</h5>
					<p>Joana Vicente has been the executive director of the Independent Filmmaker Project (IFP), the oldest and largest nonprofit organization for independent filmmakers in the United States, since 2009. Under Vicente's leadership, the IFP was most recently bestowed the honor of developing and operating the Made in NY Media Center after a Request for Proposal was issued by the Mayor's Office of Media and Entertainment and the New York City Economic Development Corporation. The state-of-the-art Media Center brings together professionals from the film, television, advertising, new media, gaming, marketing and branding industries for collaboration and new opportunities. It opened in the DUMBO neighborhood of Brooklyn, N.Y., in 2013.</p>
					<p>Prior to the IFP, Vicente and her partner Jason Kliot produced and executive produced more than 40 films by such acclaimed directors as Jim Jarmusch, Miguel Arteta, Brian De Palma, Hal Hartley, Steven Soderbergh, Nicole Holofcener and Todd Solondz. She has co-founded three separate, unique film production entities over the course of her career — Open City Films; Blow Up Pictures, the first digital production company in the United States; and HDNet Films, an award-winning digital production company founded with Mark Cuban and Todd Wagner. Among the many films that Vicente and Kliot have produced are Tony Bui's Sundance Grand Jury Prize and Audience Award winner, Three Season; Jim Jarmusch's cult classic Coffee and Cigarettes; Niels Mueller's The Assassination of Richard Nixon; Todd Solondz's Sundance Grand Jury Prize- winning Welcome to the Dollhouse; Brian De Palma's controversial Redacted and Alex Gibney's Academy Award–nominated Enron: The Smartest Guys in the Room. Vicente has served on the jury at Sundance and many other festivals.</p>
					<p>Vicente's films have garnered numerous accolades and awards, including 23 Independent Spirit Award nominations and four wins. In 2007, Vicente was the recipient of the Made in NY Award for individuals who have made outstanding contributions to New York City's entertainment industry. Vicente graduated from the master’s program at The Catholic University of Portugal with a degree in philosophy and began her career as the press attaché for the Portuguese delegate and former prime minister of Portugal at the European Parliament. She later became a radio news producer for the United Nations before turning her attention to film.</p>
				</div>
			</article>
			<article data-person="jonathan-wacks">
				<div class="picture" style="background-image:url('http://www.brooklyn.cuny.edu/web/feirsteinbeta/feirstein/build/images/Jonathan_Wacks_Council.jpg')">
				</div>
				<div class="bio">
					<h5>Jonathan Wacks</h5>
					<p>Jonathan Wacks is the founding director of the Barry R. Feirstein Graduate School of Cinema and professor of film at Brooklyn College. He has directed a number of films, including Powwow Highway (Warner Bros), produced by Beatle George Harrison. The film was the recipient of the Sundance Film Festival Filmmaker’s Trophy, nominated for four Independent Spirit Awards, and winner of awards for best picture, director and actor at the American Indian Film Festival in San Francisco.</p>
					<p>Wacks’ first film, Crossroads/South Africa (PBS), won a Student Academy Award in the documentary category. He then produced the acclaimed cult-hit Repo Man (Universal), starring Emilio Estevez and Harry Dean Stanton, and directed Mystery Date (Orion), starring Ethan Hawke and Terri Polo, and Ed and His Dead Mother, starring Steve Buscemi and Ned Beatty. He also directed an array of TV productions, including 21 Jump Street, with Johnny Depp, Sirens and Going to Extremes.</p>
					<p>Prior to his career as a director, Wacks served as vice president of production at the Samuel Goldwyn Company. He is a former chairman of the board of the Independent Feature Project/ West (now Film Independent), the largest organization of independent filmmakers in America, and has served on the selection committee of the Writers’ Program at the Sundance Institute. His work has been seen at numerous international film festivals, including Sundance, Montreal, Tokyo, Florence, London, Leipzig, Leeds, Cape Town, Deauville, New York, Munich and Berlin.</p>
					<p>Wacks has written several screenplays, including Recoil, based on the Jim Thompson novel, No Cure for Love, My African Heart, Coldsleep Lullaby and Stuck. He served as chair of the Visual and Media Arts Department at Emerson College, head of the Film Department at the Vancouver Film School in British Columbia and chair of the Moving Image Arts Department at the College of Santa Fe. He was also director of Garson Studios in Santa Fe, N.M. Wacks holds a B.A. (with honours) from the University of Essex (United Kingdom) and an M.F.A. from the UCLA School of Theater, Film and Television. He is a member of the Directors Guild of America.</p>
				</div>
