				<div class="bio">
					<h5>Celia Costas</h5>
					<p>Celia Costas won her first Emmy Award as producer of one of the most acclaimed miniseries of all time, Angels in America, directed by Mike Nichols, the Pulitzer Prize-winning playwright. In addition to the Outstanding Miniseries Emmy, the project earned 11 other Emmys, five Golden Globe Awards and two Screen Actors Guild Awards. Costas won her second Emmy Award as executive producer of Joseph Sargent’s Warm Springs, written by Margaret Nagle.</p>
					<p>She worked again with Mike Nichols as executive producer of the features Closer and Charlie Wilson’s War. She was executive producer of John Patrick Shanley’s Doubt, which was nominated for five Academy Awards. She has worked with Natalie Portman, Clive Owen, Philip Seymour Hoffman, Kenneth Brannagh and Cynthia Nixon.</p>
					<p>Costas produced the telefilm For Love or Country: The Arturo Sandoval Story and co-produced A Lesson Before Dying, which won the “Outstanding Made for Television Movie” Emmy. Her other feature credits as co-producer include Betty Thomas’ Private Parts, 28 Days and Ben Stiller’s Zoolander.</p>
